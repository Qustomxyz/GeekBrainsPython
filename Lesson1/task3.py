first_name = input('Введите ваше имя: ')
last_name = input('Введите вашу фамилию: ')
age = int(input('Сколько вам полных лет?: '))
weight = int(input('Какой ваш вес? Округлите до 1кг: '))
patient = Person(first_name, last_name, age, weight)
print(f'{patient.first_name} {patient.last_name}, {patient.age} год, вес {patient.weight} - {patient.check_health()}')


