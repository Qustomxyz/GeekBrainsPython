class Person:
    def __init__(self, first_name, last_name, age, weight):
        self.first_name = first_name
        self.last_name = last_name
        self.age = age
        self.weight = weight

    def check_health(self):
        if self.age < 30 and (50 <= self.weight < 120):
            return 'хорошее состояние'
        elif self.age > 30 and (self.weight > 120 or self.weight < 50):
            if self.age > 40:
                return 'следует обратится к врачу!'
            else:
                return 'следует заняться собой'
        else:
            return 'а у вас всё хорошо, наш преподаватель вас не заметил ...'


